	// 활용이 가능하다는 것은 new 연산자로 생성이 가능하다는 얘기임..
	
	//클래스를 선언한 내부에는 3가지 요소를 선언 할 수 있다.
	
	// 1. field (멤버변수) : 클래스에 필요한 데이터를 저장하는 공간
	// 2. 생성자 : 클래스를 객체(instance) 만들때 실행되는 기능
	// 3. 메소드 (기능) : 클래스가 제공하는 기능
	
	
